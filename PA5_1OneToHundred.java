
public class PA5_1OneToHundred {

    public static void main(String[] args) {
//     for
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
    }
//    while
            int j = 1;
            while (j <= 100) {
                System.out.println(j);
                j++;
            }
//    dowhile
            int k = 1;
            do {
                System.out.println(k);
                k++;
            } while (k <= 100);
        }

    }

